    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        if(txtID.getText().isEmpty()){
            ImageIcon icon = new ImageIcon("src/sistemaMVC/img_icones/alerta.png");
               JOptionPane.showMessageDialog(null, "Selecione um produto na tabela para deletar", "Atenção", JOptionPane.INFORMATION_MESSAGE, icon);
            JOptionPane.showMessageDialog(null, "Selecione um produto na tabela para deletar");
        }else{
        boolean exito;
        String status = cbStatus.getSelectedItem().toString();
        txtNome1.getText();
        txtADesc.getText();
        Date data_cad = txtCalendar.getDate();
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        String dataFormatada = dateFormat.format(data_cad);
        int vID = Integer.parseInt(txtID.getText());
        
        try {
            
            ProdutoController produto = new ProdutoController();
           exito = produto.excluirProduto(vID);
           if(exito == true){
               ImageIcon icone = new ImageIcon("src/sistemaMVC/img_icones/correto.png");
               JOptionPane.showMessageDialog(null, "Produto deletado com sucesso", "Exito", JOptionPane.INFORMATION_MESSAGE, icone);
               //JOptionPane.showMessageDialog(null, "Produto deletado com sucesso");
               txtID.setText("");
                txtCodBarras.setText("");
                txtCodigo.setText("");
                txtNome1.setText("");
                txtPrecoCompra.setText("");
                txtPrecoVenda.setText("");
                txtFatorLucro.setText("");
                txtEstoqueMin.setText("");
                txtEstoqueMax.setText("");
                txtQtdEstoque.setText("");
                txtNCM.setText("");
                txtADesc.setText("");
           }
        } catch (Exception e) {
            ImageIcon icone = new ImageIcon("src/sistemaMVC/img_icones/erro.png");
               JOptionPane.showMessageDialog(null, "Erro na exclusão", "Erro", JOptionPane.INFORMATION_MESSAGE, icone);
            
        }}      
    }//GEN-LAST:event_btnDeletarActionPerformed

