    private void txtFatorLucroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtFatorLucroMouseClicked
        if(txtPrecoCompra.getText().replaceAll(",", ".") != "" && txtPrecoVenda.getText().replaceAll(",", ".") !=""){
            float lucro = (Float.parseFloat(txtPrecoVenda.getText().replaceAll(",", "."))- 
                    Float.parseFloat(txtPrecoCompra.getText().replaceAll(",", ".")))/Float.parseFloat(txtPrecoVenda.getText().replaceAll(",", "."))*100;
            
            txtFatorLucro.setText(Float.toString(lucro).format("%.2f",lucro));
        }
    }//GEN-LAST:event_txtFatorLucroMouseClicked

