    public void Listar(){
        DefaultTableModel tbModel = (DefaultTableModel)tb_produto.getModel();
        tbModel.setNumRows(0);
        ProdutoController produt = new  ProdutoController();
        try {
          List<ProdutoModel> listaP = produt.listarProdutos();
          listaP.forEach((ProdutoModel produto) -> {
              tbModel.addRow(new Object[] {produto.getId(),
                                           produto.getCod(),
                                           produto.getStatus(),
                                           produto.getNome(),
                                           produto.getDescricao(),
                                           produto.getQtd_estoque(),
                                           produto.getEstoque_min(),
                                           produto.getEstoque_max(),
                                           produto.getPreco_compra(),
                                           produto.getPreco_venda(),
                                           produto.getBar_code(),
                                           produto.getNcm(),
                                           produto.getFator(),
                                           produto.getData_cadastro(),
                                           produto.getImagem()});
          });
          
        } catch (ExceptionDAO e) {
            
        }
        
    }
